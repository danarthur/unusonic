#!/usr/bin/env node
// ESLint baseline ratchet.
//
// Usage:
//   node scripts/eslint-baseline.mjs --snapshot    Snapshot current violations
//                                                  to .eslint-baseline.json.
//   node scripts/eslint-baseline.mjs --check       Fail if any file has more
//                                                  violations than baseline.
//                                                  (Ignored rules, added rules,
//                                                  and files outside the
//                                                  baseline are allowed if
//                                                  their counts are zero or
//                                                  unchanged.)
//
// Ratchet semantics:
//   - Baseline records { filePath: { ruleId: count } } for every WARN rule.
//     Errors are never allowed; ESLint exits non-zero for those separately.
//   - On --check: current file+rule count must be <= baseline count.
//   - If a file is not in baseline, its count must be zero (new files can't
//     exceed the limit since Day 0).
//   - If a rule is not in baseline for a file, current count must be zero.
//
// Philosophy: legacy files may stay broken. They cannot get worse. New files
// must comply. See docs/reference/code/coding-protocol.md §6 for rationale.

import { ESLint } from "eslint";
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { relative } from "node:path";
import { fileURLToPath } from "node:url";

const REPO_ROOT = fileURLToPath(new URL("..", import.meta.url));
const BASELINE_PATH = new URL("../.eslint-baseline.json", import.meta.url)
  .pathname;

const mode = process.argv[2];
if (mode !== "--snapshot" && mode !== "--check") {
  console.error("Usage: eslint-baseline.mjs --snapshot | --check");
  process.exit(2);
}

const eslint = new ESLint();
const results = await eslint.lintFiles(["src/**/*.{ts,tsx}"]);

// Build { relativeFilePath: { ruleId: count } } from ESLint results.
// Only warnings count for the ratchet — errors are hard-fail from ESLint.
const current = {};
for (const result of results) {
  const rel = relative(REPO_ROOT, result.filePath);
  for (const msg of result.messages) {
    if (msg.severity !== 1) continue; // 1 = warning, 2 = error
    const rule = msg.ruleId ?? "_unknown";
    current[rel] ??= {};
    current[rel][rule] = (current[rel][rule] ?? 0) + 1;
  }
}

if (mode === "--snapshot") {
  // Write in stable key order for clean diffs.
  const sortedFiles = Object.keys(current).sort();
  const out = {};
  for (const f of sortedFiles) {
    const sortedRules = Object.keys(current[f]).sort();
    out[f] = {};
    for (const r of sortedRules) out[f][r] = current[f][r];
  }
  writeFileSync(
    BASELINE_PATH,
    JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        note: "Generated by scripts/eslint-baseline.mjs --snapshot. DO NOT edit by hand. Regenerate after any refactor that reduces violations.",
        files: out,
      },
      null,
      2,
    ) + "\n",
  );
  const fileCount = sortedFiles.length;
  const ruleCount = sortedFiles.reduce(
    (acc, f) => acc + Object.keys(current[f]).length,
    0,
  );
  const violationCount = sortedFiles.reduce(
    (acc, f) =>
      acc + Object.values(current[f]).reduce((a, b) => a + b, 0),
    0,
  );
  console.log(
    `baseline snapshot: ${fileCount} files, ${ruleCount} file/rule pairs, ${violationCount} total warnings`,
  );
  process.exit(0);
}

// --check mode
if (!existsSync(BASELINE_PATH)) {
  console.error(
    "No baseline at .eslint-baseline.json. Run --snapshot first.",
  );
  process.exit(2);
}
const baseline = JSON.parse(readFileSync(BASELINE_PATH, "utf8")).files ?? {};

const regressions = [];
for (const [file, rules] of Object.entries(current)) {
  const baselineRules = baseline[file] ?? {};
  for (const [rule, count] of Object.entries(rules)) {
    const allowed = baselineRules[rule] ?? 0;
    if (count > allowed) {
      regressions.push({ file, rule, count, allowed });
    }
  }
}

if (regressions.length === 0) {
  // Also report reductions so engineers can regenerate.
  let reductionCount = 0;
  for (const [file, rules] of Object.entries(baseline)) {
    for (const [rule, allowed] of Object.entries(rules)) {
      const count = current[file]?.[rule] ?? 0;
      if (count < allowed) reductionCount++;
    }
  }
  console.log(
    `✅ baseline check passed. ${reductionCount > 0 ? `${reductionCount} file/rule improvements detected — consider running --snapshot to shrink baseline.` : "No regressions."}`,
  );
  process.exit(0);
}

console.error(`❌ ESLint baseline regressions (${regressions.length}):\n`);
for (const r of regressions) {
  console.error(
    `  ${r.file} [${r.rule}]: ${r.count} warnings (baseline: ${r.allowed})`,
  );
}
console.error(
  "\nThese files have MORE violations than the baseline allows. Fix or split before merging.",
);
console.error(
  "If the regression is intentional (e.g. extracted subcomponent is now the largest file), the refactor should still REDUCE total violations — re-snapshot after.",
);
process.exit(1);
